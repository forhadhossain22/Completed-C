// Examples of arithmetic operators in C

#include <stdio.h>

int main()

{

    int a = 7,b = 5, c;

    c = a+b;

    printf("a+b = %d \n",c);

    c = a-b;

    printf("a-b = %d \n",c);

    c = a*b;

    printf("a*b = %d \n",c);

    c = a/b;

    printf("a/b = %d \n",c);

    c = a%b;

    printf("Remainder when a is divided by b = %d \n",c);

    return 0;

}
